function show_grade(){
    const element_in = document.getElementById("detail_restaurant");
    const result = grade.find(fruit => fruit.restaurantname_grade === element_in.innerText);
    if (result===undefined){
        const element_grade = document.getElementById("restaurant_grade");
        element_grade.innerHTML = "위 식당은 위생등급을 제공하지 않습니다.";
    }
    else{
        const element_grade = document.getElementById("restaurant_grade");
        element_grade.innerHTML = "위생등급은 "+result.food_grade+" 입니다";
    }    
}

function filter(){
    var value, grade_container_name, grade_container_item, i;
    var value, name, item, i;
    value = document.getElementById("value").value.toUpperCase();
    grade_container_item = document.getElementsByClassName("grade_container_item");

    for(i=0;i<grade_container_item.length;i++){
        grade_container_name = grade_container_item[i].getElementsByClassName("grade_container_name");
        if(grade_container_name[0].innerHTML.toUpperCase().indexOf(value) > -1){
        grade_container_item[i].style.display = "flex";
        }else{
        grade_container_item[i].style.display = "none";
        }
    }
}

function getInnerText() {
    $(document).on("click",".grade_container_name",function(){
        var clicked_place = $(this).text();

        let result = grade;

        for (var i = 0; i < result.length; i++) {
            if (result[i].restaurantname_grade === clicked_place) {
                var result_restaurant = result[i].food_grade
                const unsanitary_result_phrase = document.getElementById("unsanitary_result");
                unsanitary_result_phrase.innerHTML = clicked_place + "의 위생등급은 " + result_restaurant + " 입니다."
            }
        }
    });
}